public class basicIfElseTutorial {
    public static void main(String[] args) {
        //This tutorial we will cover the importance of if-else statements. 
        //first we will be defining some variables that we will be using for basic comparisons
        //Later in another file below, we will be giving you some questions that you can solve 
        //with if-else statements to test your knowledge.
        int firstNumber = 23;
        int secondNumber = 1;
        if(firstNumber>secondNumber)
        {
            System.out.println("The first number is bigger then the second number!");
        }
        else
        {
            System.out.println("The second number is bigger then the first number!");
        }
        //You are able to change any of the integer values to be anything you would like it to be. 
        //Try it with different numbers now by making the first number bigger then the second number and watch the output in the terminal change.
    }
}
